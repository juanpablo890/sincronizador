    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    try {
        String logFileText="";
        Calendar calendar;
        calendar = jDateChooser1.getCalendar();
        System.out.println("fecha: "+calendar.getTime());
        LocalDate ofInstant = LocalDate.ofInstant(calendar.toInstant(), ZoneId.systemDefault());
        String fecha = ofInstant.format(DateTimeFormatter.ofPattern("dd-MM-yyyy"));
        
        List<String> readLoggerFile = new Logger().readLoggerFile(Path.of("motor_tisa_"+fecha+".log"));
        for (String string : readLoggerFile) {
            logFileText+=string+"\n";
        }
        fileText.setText(logFileText);
    } catch (IOException ex) {
        JOptionPane.showMessageDialog(rootPane, "No se encontr√≥ un archivo de log para la fecha seleccionada", "Error al leer archivo de log", JOptionPane.ERROR_MESSAGE);
        MotorTISA.getLogger().log(Level.SEVERE, null, ex);
    }
    catch(NullPointerException ex){
        JOptionPane.showMessageDialog(rootPane, "Debe seleccionar una fecha del calendario", "Fecha no seleccionada", JOptionPane.ERROR_MESSAGE);
        MotorTISA.getLogger().log(Level.SEVERE, null, ex);
    }
    }//GEN-LAST:event_jButton1ActionPerformed

